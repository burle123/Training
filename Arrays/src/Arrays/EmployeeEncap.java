package Arrays;

public class EmployeeEncap {
	
	private String empname;
	private int empid;
	private int salary;
	
	public String getName() {
		return empname;
	}
	public int getId() {
		return empid;
	}
	public int getSalary() {
		return salary;
	}
	
	public void setName(String empname) {
		this.empname=empname;
	}
	public void setId(int empid) {
		this.empid=empid;
	}
	public void setSal(int salary) {
		this.salary=salary;
	}

	

}
